def process_image_data(url_list, image_id):
    """Retrieve and load a specific image from the provided URL list"""
    image = imageio.imread(url_list[image_id])
    return image


def apply_cartoon_augmentation(image):
    """Apply cartoon-style augmentation to the input image"""
    augs = [image]
    augs += iaa.Cartoon()(images=[image] * 15)
    return augs


def visualize_augmented_images(augs, grid_size):
    """Display augmented images in a grid layout"""
    ia.imshow(ia.draw_grid(augs, grid_size[0], grid_size[1]))


def run_image_processing_pipeline():
    """Execute the full image processing pipeline"""
    # Retrieve and select image from medium resolution list
    image = process_image_data(
        __init_url_data_structures()[1], 0
    )
    
    # Apply augmented transformations
    augs = apply_cartoon_augmentation(image)
    
    # Visualize the result in a 4x4 grid
    visualize_augmented_images(augs, (4, 4))


if __name__ == "__main__":
    run_image_processing_pipeline()